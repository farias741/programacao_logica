# Calculadora de inteiros

# O sistema pede a entrada do primeiro valor, do tipo inteiro
# O sistema pede a entrada do segundo valor, do tipo inteiro
# O sistema imprime:
    # A soma dos dois valores
    # O produto dos valores

primeiro =  int(input("Informe o primeiro valor: "))
segundo =  int(input("Informe o segundo valor: "))
soma = primeiro + segundo
produto = primeiro * segundo
print("A soma dos valores é : ",soma, " e o produtos dos valores é ", produto)